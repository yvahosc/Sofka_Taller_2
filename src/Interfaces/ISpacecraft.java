package Interfaces;

/**
 * Representa la interface nave espacial y sus métodos.
 * @author Yeisson Vahos Cortes
 */
public interface ISpacecraft {

    /**
     * Representa la declaración del método para cargar combustibles a las
     * naves espaciales.
     */
    public void refuelingSpacecraft();

    /**
     * Representa la declaración del método para encender las naves espaciales. 
     */
    public void turnOnSpacecraft();

}
